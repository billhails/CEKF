static void initVisitorContext(VisitorContext *child, VisitorContext *parent,
                               AstSyntaxDecl *syntaxDecl) {
    child->next = parent;
    child->syntaxDecl = syntaxDecl;
}

static AstSyntaxDecl *findSyntaxDeclaration(int declarationId,
                                            VisitorContext *context) {
    while (context != NULL) {
        if (context->syntaxDecl != NULL &&
            context->syntaxDecl->declarationId == declarationId) {
            return context->syntaxDecl;
        }
        context = context->next;
    }
    return NULL;
}

static AstSyntaxBinding *findSyntaxBinding(HashSymbol *name,
                                           AstSyntaxBindings *bindings) {
    if (bindings == NULL) {
        return NULL;
    }

    for (Index i = 0; i < bindings->size; i++) {
        AstSyntaxBinding *binding = peeknAstSyntaxBindings(bindings, i);
        if (binding->name == name) {
            return binding;
        }
    }

    return NULL;
}

static HashSymbol *binderKeySymbol(int binderId) {
    char buffer[64];

    snprintf(buffer, sizeof(buffer), "__syntax_binder_%d", binderId);
    return newSymbol(buffer);
}

static HashSymbol *getIntroducedSymbol(int binderId,
                                       ExpansionContext *context) {
    HashSymbol *resolved = NULL;

    if (!getSymbolMap(context->introducedNames, binderKeySymbol(binderId),
                      &resolved)) {
        cant_happen("unbound introduced binder %d during syntax lowering",
                    binderId);
    }

    return resolved;
}

static HashSymbol *internIntroducedSymbol(AstSyntaxIntroducedBinder *node,
                                          ExpansionContext *context) {
    HashSymbol *key = binderKeySymbol(node->binderId);
    HashSymbol *resolved = NULL;

    if (!getSymbolMap(context->introducedNames, key, &resolved)) {
        resolved = genSymDollar(node->writtenName->name);
        setSymbolMap(context->introducedNames, key, resolved);
    }

    return resolved;
}

static AstSyntaxAlternative *getSyntaxAlternative(AstSyntaxDecl *declaration,
                                                  int alternativeIndex) {
    if (declaration == NULL || declaration->alternatives == NULL ||
        alternativeIndex < 0 ||
        (Index)alternativeIndex >= declaration->alternatives->size) {
        cant_happen("invalid syntax alternative %d for declaration %d",
                    alternativeIndex,
                    declaration != NULL ? declaration->declarationId : -1);
    }

    return peeknAstSyntaxAlternatives(declaration->alternatives,
                                      (Index)alternativeIndex);
}

static AstLookUpSymbol *copyOrReuseLookUpSymbol(AstLookUpSymbol *node) {
    return node;
}

static HashSymbol *instantiateTemplateBinder(AstSyntaxTemplateBinder *node,
                                             ExpansionContext *context) {
    if (node == NULL) {
        cant_happen("unexpected NULL syntax template binder during lowering");
    }

    switch (node->type) {
    case AST_SYNTAXTEMPLATEBINDER_TYPE_INTRODUCED:
        return internIntroducedSymbol(
            getAstSyntaxTemplateBinder_Introduced(node), context);
    case AST_SYNTAXTEMPLATEBINDER_TYPE_UNQUOTE: {
        AstSyntaxUnquote *unquote = getAstSyntaxTemplateBinder_Unquote(node);
        AstSyntaxBinding *binding =
            findSyntaxBinding(unquote->bindingName, context->bindings);

        if (binding == NULL || binding->value == NULL ||
            binding->value->type != AST_EXPRESSION_TYPE_SYMBOL) {
            cant_happen("syntax binder unquote %s did not resolve to a name",
                        unquote->bindingName->name);
        }

        return getAstExpression_Symbol(binding->value);
    }
    default:
        cant_happen("unrecognized AstSyntaxTemplateBinder type %d", node->type);
    }
}

static AstExpression *instantiateTemplateNameRef(AstSyntaxTemplateNameRef *node,
                                                 ExpansionContext *context) {
    if (node == NULL) {
        return NULL;
    }

    switch (node->type) {
    case AST_SYNTAXTEMPLATENAMEREF_TYPE_LITERAL: {
        AstSyntaxLiteralRef *literal =
            getAstSyntaxTemplateNameRef_Literal(node);
        HashSymbol *resolved = literal->resolvedName != NULL
                                   ? literal->resolvedName
                                   : literal->writtenName;
        return newAstExpression_Symbol(CPI(node), resolved);
    }
    case AST_SYNTAXTEMPLATENAMEREF_TYPE_INTRODUCED: {
        AstSyntaxIntroducedRef *introduced =
            getAstSyntaxTemplateNameRef_Introduced(node);
        return newAstExpression_Symbol(
            CPI(node), getIntroducedSymbol(introduced->binderId, context));
    }
    case AST_SYNTAXTEMPLATENAMEREF_TYPE_UNQUOTE: {
        AstSyntaxUnquote *unquote = getAstSyntaxTemplateNameRef_Unquote(node);
        AstSyntaxBinding *binding =
            findSyntaxBinding(unquote->bindingName, context->bindings);

        if (binding == NULL || binding->value == NULL) {
            cant_happen("missing syntax binding %s during lowering",
                        unquote->bindingName->name);
        }

        return copyAstExpression(binding->value);
    }
    default:
        cant_happen("unrecognized AstSyntaxTemplateNameRef type %d",
                    node->type);
    }
}

static AstExpressions *
instantiateTemplateExprsFrom(AstSyntaxTemplateExprs *node, Index index,
                             ExpansionContext *context) {
    if (node == NULL || index >= node->size) {
        return NULL;
    }

    AstSyntaxTemplateExpr *element = peeknAstSyntaxTemplateExprs(node, index);
    AstExpression *expression = instantiateTemplateExpr(element, context);
    int save = PROTECT(expression);
    AstExpressions *next =
        instantiateTemplateExprsFrom(node, index + 1, context);
    PROTECT(next);
    AstExpressions *result = newAstExpressions(CPI(element), expression, next);
    UNPROTECT(save);
    return result;
}

static AstExpressions *instantiateTemplateExprs(AstSyntaxTemplateExprs *node,
                                                ExpansionContext *context) {
    return instantiateTemplateExprsFrom(node, 0, context);
}

