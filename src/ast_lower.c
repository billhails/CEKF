static AstCompositeFunction *
prependTemplateCompositeFunction(AstSyntaxTemplateAltFunction *function,
                                 AstCompositeFunction *rest,
                                 ExpansionContext *context) {
    int save = STARTPROTECT();

    for (AstSyntaxTemplateAltArgs *args = function->altArgs; args != NULL;
         args = args->next) {
        AstFargList *argList =
            instantiateTemplateFargList(args->argList, context);
        PROTECT(argList);
        AstNest *nest = instantiateTemplateNest(function->nest, context);
        PROTECT(nest);
        AstFunction *current = newAstFunction(CPI(args), argList, nest);
        PROTECT(current);
        rest = newAstCompositeFunction(CPI(args), current, rest);
        PROTECT(rest);
    }

    UNPROTECT(save);
    return rest;
}

static AstCompositeFunction *
instantiateTemplateCompositeFunction(AstSyntaxTemplateFunction *node,
                                     ExpansionContext *context) {
    if (node == NULL) {
        return NULL;
    }

    AstCompositeFunction *rest =
        instantiateTemplateCompositeFunction(node->next, context);
    int save = PROTECT(rest);
    AstCompositeFunction *result =
        prependTemplateCompositeFunction(node->function, rest, context);
    if (result != NULL) {
        result->unsafe = node->unsafe;
    }
    UNPROTECT(save);
    return result;
}

static AstFarg *instantiateTemplateFarg(AstSyntaxTemplateFarg *node,
                                        ExpansionContext *context) {
    if (node == NULL) {
        return NULL;
    }

    switch (node->type) {
    case AST_SYNTAXTEMPLATEFARG_TYPE_WILDCARD:
        return newAstFarg_WildCard(CPI(node));
    case AST_SYNTAXTEMPLATEFARG_TYPE_BINDER:
        return newAstFarg_Symbol(
            CPI(node), instantiateTemplateBinder(
                           getAstSyntaxTemplateFarg_Binder(node), context));
    case AST_SYNTAXTEMPLATEFARG_TYPE_LOOKUP:
        return newAstFarg_LookUp(
            CPI(node),
            copyOrReuseLookUpSymbol(getAstSyntaxTemplateFarg_LookUp(node)));
    case AST_SYNTAXTEMPLATEFARG_TYPE_NAMED: {
        AstSyntaxTemplateNamedArg *named = getAstSyntaxTemplateFarg_Named(node);
        AstFarg *arg = instantiateTemplateFarg(named->arg, context);
        int save = PROTECT(arg);
        AstNamedArg *result = newAstNamedArg(CPI(named), named->name, arg);
        PROTECT(result);
        AstFarg *wrapped = newAstFarg_Named(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEFARG_TYPE_UNPACK: {
        AstSyntaxTemplateUnpack *unpack = getAstSyntaxTemplateFarg_Unpack(node);
        AstLookUpOrSymbol *symbol = copyAstLookUpOrSymbol(unpack->symbol);
        int save = PROTECT(symbol);
        AstFargList *argList =
            instantiateTemplateFargList(unpack->argList, context);
        PROTECT(argList);
        AstUnpack *result = newAstUnpack(CPI(unpack), symbol, argList);
        PROTECT(result);
        AstFarg *wrapped = newAstFarg_Unpack(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEFARG_TYPE_UNPACKSTRUCT: {
        AstSyntaxTemplateUnpackStruct *unpack =
            getAstSyntaxTemplateFarg_UnpackStruct(node);
        AstLookUpOrSymbol *symbol = copyAstLookUpOrSymbol(unpack->symbol);
        int save = PROTECT(symbol);
        AstTaggedArgList *argList =
            instantiateTemplateTaggedArgs(unpack->argList, context);
        PROTECT(argList);
        AstUnpackStruct *result =
            newAstUnpackStruct(CPI(unpack), symbol, argList);
        PROTECT(result);
        AstFarg *wrapped = newAstFarg_UnpackStruct(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEFARG_TYPE_NUMBER:
        return newAstFarg_Number(CPI(node),
                                 getAstSyntaxTemplateFarg_Number(node));
    case AST_SYNTAXTEMPLATEFARG_TYPE_CHARACTER:
        return newAstFarg_Character(CPI(node),
                                    getAstSyntaxTemplateFarg_Character(node));
    case AST_SYNTAXTEMPLATEFARG_TYPE_TUPLE:
        return newAstFarg_Tuple(
            CPI(node), instantiateTemplateFargList(
                           getAstSyntaxTemplateFarg_Tuple(node), context));
    default:
        cant_happen("unrecognized AstSyntaxTemplateFarg type %d", node->type);
    }
}

static AstExpression *instantiateTemplateExpr(AstSyntaxTemplateExpr *node,
                                              ExpansionContext *context) {
    if (node == NULL) {
        return NULL;
    }

    switch (node->type) {
    case AST_SYNTAXTEMPLATEEXPR_TYPE_BACK:
        return newAstExpression_Back(CPI(node));
    case AST_SYNTAXTEMPLATEEXPR_TYPE_WILDCARD:
        return newAstExpression_WildCard(CPI(node));
    case AST_SYNTAXTEMPLATEEXPR_TYPE_NAMEREF:
        return instantiateTemplateNameRef(
            getAstSyntaxTemplateExpr_NameRef(node), context);
    case AST_SYNTAXTEMPLATEEXPR_TYPE_NUMBER:
        return newAstExpression_Number(CPI(node),
                                       getAstSyntaxTemplateExpr_Number(node));
    case AST_SYNTAXTEMPLATEEXPR_TYPE_CHARACTER:
        return newAstExpression_Character(
            CPI(node), getAstSyntaxTemplateExpr_Character(node));
    case AST_SYNTAXTEMPLATEEXPR_TYPE_FUNCALL: {
        AstSyntaxTemplateFunCall *call = getAstSyntaxTemplateExpr_FunCall(node);
        AstExpression *function =
            instantiateTemplateExpr(call->function, context);
        int save = PROTECT(function);
        AstExpressions *arguments =
            instantiateTemplateExprs(call->arguments, context);
        PROTECT(arguments);
        AstFunCall *result = newAstFunCall(CPI(call), function, arguments);
        PROTECT(result);
        AstExpression *wrapped = newAstExpression_FunCall(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_LOOKUP: {
        AstSyntaxTemplateLookUp *lookUp = getAstSyntaxTemplateExpr_LookUp(node);
        return instantiateTemplateExpr(lookUp->expression, context);
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_FUN: {
        AstCompositeFunction *function = instantiateTemplateCompositeFunction(
            getAstSyntaxTemplateExpr_Fun(node), context);
        int save = PROTECT(function);
        AstExpression *result = newAstExpression_Fun(CPI(node), function);
        UNPROTECT(save);
        return result;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_NEST: {
        AstNest *nest = instantiateTemplateNest(
            getAstSyntaxTemplateExpr_Nest(node), context);
        int save = PROTECT(nest);
        AstExpression *result = newAstExpression_Nest(CPI(node), nest);
        UNPROTECT(save);
        return result;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_IFF: {
        AstSyntaxTemplateIff *iff = getAstSyntaxTemplateExpr_Iff(node);
        AstExpression *test = instantiateTemplateExpr(iff->test, context);
        int save = PROTECT(test);
        AstNest *consequent = instantiateTemplateNest(iff->consequent, context);
        PROTECT(consequent);
        AstNest *alternative =
            instantiateTemplateNest(iff->alternative, context);
        PROTECT(alternative);
        AstIff *result = newAstIff(CPI(iff), test, consequent, alternative);
        PROTECT(result);
        AstExpression *wrapped = newAstExpression_Iff(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_PRINT: {
        AstSyntaxTemplatePrint *print = getAstSyntaxTemplateExpr_Print(node);
        AstExpression *exp =
            instantiateTemplateExpr(print->expression, context);
        int save = PROTECT(exp);
        AstPrint *result = newAstPrint(CPI(print), exp);
        PROTECT(result);
        AstExpression *wrapped = newAstExpression_Print(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_TYPEOF: {
        AstSyntaxTemplateTypeOf *typeOf = getAstSyntaxTemplateExpr_TypeOf(node);
        AstExpression *exp =
            instantiateTemplateExpr(typeOf->expression, context);
        int save = PROTECT(exp);
        AstTypeOf *result = newAstTypeOf(CPI(typeOf), exp);
        PROTECT(result);
        AstExpression *wrapped = newAstExpression_TypeOf(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_TUPLE:
        return newAstExpression_Tuple(
            CPI(node), instantiateTemplateExprs(
                           getAstSyntaxTemplateExpr_Tuple(node), context));
    case AST_SYNTAXTEMPLATEEXPR_TYPE_STRUCTURE: {
        AstSyntaxTemplateStruct *structure =
            getAstSyntaxTemplateExpr_Structure(node);
        AstLookUpOrSymbol *symbol = copyAstLookUpOrSymbol(structure->symbol);
        int save = PROTECT(symbol);
        AstTaggedExpressions *expressions =
            instantiateTemplateTaggedExpressions(structure->expressions,
                                                 context);
        PROTECT(expressions);
        AstStruct *result = newAstStruct(CPI(structure), symbol, expressions);
        PROTECT(result);
        AstExpression *wrapped = newAstExpression_Structure(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_ASSERTION: {
        AstExpression *exp = instantiateTemplateExpr(
            getAstSyntaxTemplateExpr_Assertion(node), context);
        int save = PROTECT(exp);
        AstExpression *result = newAstExpression_Assertion(CPI(node), exp);
        UNPROTECT(save);
        return result;
    }
    case AST_SYNTAXTEMPLATEEXPR_TYPE_ERROR: {
        AstExpression *exp = instantiateTemplateExpr(
            getAstSyntaxTemplateExpr_Error(node), context);
        int save = PROTECT(exp);
        AstExpression *result = newAstExpression_Error(CPI(node), exp);
        UNPROTECT(save);
        return result;
    }
    default:
        cant_happen("unrecognized AstSyntaxTemplateExpr type %d", node->type);
    }
}

static AstDefinition *
instantiateTemplateDefinition(AstSyntaxTemplateDefinition *node,
                              ExpansionContext *context) {
    if (node == NULL) {
        return NULL;
    }

    switch (node->type) {
    case AST_SYNTAXTEMPLATEDEFINITION_TYPE_DEFINE: {
        AstSyntaxTemplateDefine *define =
            getAstSyntaxTemplateDefinition_Define(node);
        HashSymbol *symbol = instantiateTemplateBinder(define->symbol, context);
        int save = STARTPROTECT();
        AstExpression *expression =
            instantiateTemplateExpr(define->expression, context);
        PROTECT(expression);
        AstDefine *result = newAstDefine(CPI(define), symbol, expression);
        PROTECT(result);
        AstDefinition *wrapped = newAstDefinition_Define(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEDEFINITION_TYPE_MULTI: {
        AstSyntaxTemplateMultiDefine *multi =
            getAstSyntaxTemplateDefinition_Multi(node);
        AstSymbolList *symbols =
            instantiateTemplateSymbolList(multi->symbols, context);
        int save = PROTECT(symbols);
        AstExpression *expression =
            instantiateTemplateExpr(multi->expression, context);
        PROTECT(expression);
        AstMultiDefine *result =
            newAstMultiDefine(CPI(multi), symbols, expression);
        PROTECT(result);
        AstDefinition *wrapped = newAstDefinition_Multi(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEDEFINITION_TYPE_LAZY: {
        AstSyntaxTemplateDefLazy *lazy =
            getAstSyntaxTemplateDefinition_Lazy(node);
        HashSymbol *name = instantiateTemplateBinder(lazy->name, context);
        int save = STARTPROTECT();
        AstAltFunction *definition =
            instantiateTemplateAltFunction(lazy->definition, context);
        PROTECT(definition);
        AstDefLazy *result = newAstDefLazy(CPI(lazy), name, definition);
        PROTECT(result);
        AstDefinition *wrapped = newAstDefinition_Lazy(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEDEFINITION_TYPE_ALIAS: {
        AstSyntaxTemplateAlias *alias =
            getAstSyntaxTemplateDefinition_Alias(node);
        AstAlias *result = newAstAlias(
            CPI(alias), instantiateTemplateBinder(alias->name, context),
            alias->type);
        int save = PROTECT(result);
        AstDefinition *wrapped = newAstDefinition_Alias(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    case AST_SYNTAXTEMPLATEDEFINITION_TYPE_TYPEDEF: {
        AstSyntaxTemplateTypeDef *typeDef =
            getAstSyntaxTemplateDefinition_TypeDef(node);
        AstTypeDef *result =
            newAstTypeDef(CPI(typeDef), typeDef->typeSig, typeDef->typeBody);
        int save = PROTECT(result);
        AstDefinition *wrapped = newAstDefinition_TypeDef(CPI(node), result);
        UNPROTECT(save);
        return wrapped;
    }
    default:
        cant_happen("unrecognized AstSyntaxTemplateDefinition type %d",
                    node->type);
    }
}

static AstDefinitions *
instantiateTemplateDefinitions(AstSyntaxTemplateDefinitions *node,
                               ExpansionContext *context) {
    if (node == NULL) {
        return NULL;
    }

    AstDefinition *definition =
        instantiateTemplateDefinition(node->definition, context);
    int save = PROTECT(definition);
    AstDefinitions *next = instantiateTemplateDefinitions(node->next, context);
    PROTECT(next);
    AstDefinitions *result = newAstDefinitions(CPI(node), definition, next);
    UNPROTECT(save);
    return result;
}

static AstExpression *instantiateSyntaxUseExpression(AstExprSyntaxUse *node,
                                                     VisitorContext *context) {
    AstSyntaxDecl *declaration =
        findSyntaxDeclaration(node->declarationId, context);

    if (declaration == NULL ||
        declaration->resultKind != AST_SYNTAXRESULTKIND_TYPE_EXPR) {
        cant_happen("invalid syntax expression use for declaration %d",
                    node->declarationId);
    }

    AstSyntaxAlternative *alternative =
        getSyntaxAlternative(declaration, node->alternativeIndex);
    if (alternative->template == NULL || alternative->template->expr == NULL) {
        cant_happen("syntax expression template missing for declaration %d",
                    node->declarationId);
    }

    SymbolMap *introducedNames = newSymbolMap();
    int save = PROTECT(introducedNames);
    ExpansionContext expansion = {
        .bindings = node->bindings,
        .introducedNames = introducedNames,
    };
    AstExpression *expanded =
        instantiateTemplateExpr(alternative->template->expr, &expansion);
    PROTECT(expanded);
    AstExpression *result = lowerAstExpression(expanded, context);
    PROTECT(result);
    UNPROTECT(save);
    return result;
}

static AstDefinition *instantiateSyntaxUseDefinition(AstDefSyntaxUse *node,
                                                     VisitorContext *context) {
    AstSyntaxDecl *declaration =
        findSyntaxDeclaration(node->declarationId, context);

    if (declaration == NULL ||
        declaration->resultKind != AST_SYNTAXRESULTKIND_TYPE_DEF) {
        cant_happen("invalid syntax definition use for declaration %d",
                    node->declarationId);
    }

    AstSyntaxAlternative *alternative =
        getSyntaxAlternative(declaration, node->alternativeIndex);
    if (alternative->template == NULL ||
        alternative->template->definition == NULL) {
        cant_happen("syntax definition template missing for declaration %d",
                    node->declarationId);
    }

    SymbolMap *introducedNames = newSymbolMap();
    int save = PROTECT(introducedNames);
    ExpansionContext expansion = {
        .bindings = node->bindings,
        .introducedNames = introducedNames,
    };
    AstDefinition *expanded = instantiateTemplateDefinition(
        alternative->template->definition, &expansion);
    PROTECT(expanded);
    AstDefinition *result = lowerAstDefinition(expanded, context);
    PROTECT(result);
    UNPROTECT(save);
    return result;
}

static AstExpressions *
lowerAstExpressionsWithVisibleSyntaxDecls(AstDefinitions *definitions,
                                          AstExpressions *expressions,
                                          VisitorContext *context) {
    if (definitions == NULL) {
        return lowerAstExpressions(expressions, context);
    }

    if (definitions->definition != NULL &&
        definitions->definition->type == AST_DEFINITION_TYPE_SYNTAXDECL) {
        VisitorContext child;

        initVisitorContext(
            &child, context,
            getAstDefinition_SyntaxDecl(definitions->definition));
        return lowerAstExpressionsWithVisibleSyntaxDecls(definitions->next,
                                                         expressions, &child);
    }

    return lowerAstExpressionsWithVisibleSyntaxDecls(definitions->next,
                                                     expressions, context);
}

