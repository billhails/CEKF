static AstProg *lowerAstProg(AstProg *node, VisitorContext *context) {
    if (node == NULL)
        return NULL;
    ENTER(lowerAstProg);
    bool changed = false;
    AstDefinitions *new_preamble = lowerAstDefinitions(node->preamble, context);
    int save = PROTECT(new_preamble);
    changed = changed || (new_preamble != node->preamble);
    AstNameSpaceArray *new_nameSpaces =
        lowerAstNameSpaceArray(node->nameSpaces, context);
    PROTECT(new_nameSpaces);
    changed = changed || (new_nameSpaces != node->nameSpaces);
    AstExpressions *new_body = lowerAstExpressionsWithVisibleSyntaxDecls(
        node->preamble, node->body, context);
    PROTECT(new_body);
    changed = changed || (new_body != node->body);
    AstProg *result = node;
    if (changed) {
        result = newAstProg(CPI(node), new_preamble, new_nameSpaces, new_body);
    }
    UNPROTECT(save);
    LEAVE(lowerAstProg);
    return result;
}

