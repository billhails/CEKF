static AstDefinitions *lowerAstDefinitions(AstDefinitions *node,
                                           VisitorContext *context) {
    if (node == NULL)
        return NULL;
    ENTER(lowerAstDefinitions);
    VisitorContext child;
    VisitorContext *nextContext = context;
    if (node->definition != NULL &&
        node->definition->type == AST_DEFINITION_TYPE_SYNTAXDECL) {
        initVisitorContext(&child, context,
                           getAstDefinition_SyntaxDecl(node->definition));
        nextContext = &child;
    }
    bool changed = false;
    AstDefinition *new_definition =
        lowerAstDefinition(node->definition, context);
    int save = PROTECT(new_definition);
    changed = changed || (new_definition != node->definition);
    AstDefinitions *new_next = lowerAstDefinitions(node->next, nextContext);
    PROTECT(new_next);
    changed = changed || (new_next != node->next);
    if (node->definition != NULL &&
        node->definition->type == AST_DEFINITION_TYPE_SYNTAXDECL) {
        UNPROTECT(save);
        LEAVE(lowerAstDefinitions);
        return new_next;
    }
    AstDefinitions *result = node;
    if (changed) {
        result = newAstDefinitions(CPI(node), new_definition, new_next);
    }
    UNPROTECT(save);
    LEAVE(lowerAstDefinitions);
    return result;
}

